def parse_matrix(rows_str):
    """Extract integer rows from a verbatim 'matrix rows' block.
    Each printed line ends with the only [...] list on the line."""
    M = []
    for line in rows_str.strip().splitlines():
        m = re.search(r'\[([\d,\s]+)\]', line)
        if not m:
            continue
        M.append([int(t) for t in m.group(1).split(',')])
    return M

def parse_evec(evec_str):
    """Extract |Aut| weights e_i from the 'vertex | e=|Aut| :' tail."""
    return [int(t) for t in re.findall(r':(\d+)', evec_str)]

def parse_charpoly(cp_str):
    """Sage factor() string -> sympy polynomial in x."""
    s = cp_str.replace('^', '**')
    return sp.expand(sp.sympify(s))

def minpoly_of_double(q_in_y):
    """Given monic q(y) = minpoly of alpha, return monic minpoly of 2*alpha."""
    q = sp.Poly(q_in_y, y)
    d = q.degree()
    t = sp.expand(q.as_expr().subs(y, y/2) * 2**d)
    return sp.Poly(t, y).monic().as_expr()


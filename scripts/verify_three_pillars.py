    quo, rem = sp.div(sp.Poly(cp_recomputed, x), sp.Poly(base, x))
    assert rem == 0, (p, "catalogue: base does not divide charpoly")
    residue = quo.as_expr()
    # residue should be a perfect square in Z[x]
    rsqrt = sp.sqrt(sp.factor(residue))
    # robust perfect-square test via factorization of the Poly:
    pres = sp.Poly(residue, x)
    is_sq = all(m % 2 == 0 for (_, m) in pres.factor_list()[1])
    assert is_sq, (p, "residue not a perfect square", sp.factor(residue))
    # compute the actual square root polynomial:
    sq = sp.Integer(pres.factor_list()[0])
    for (fac, m) in pres.factor_list()[1]:
        sq *= fac.as_expr()**(m//2)
    mys_derived = sp.expand(sq)
    mys_table = sp.expand(sp.sympify(d['Mys']))
    # compare up to sign (square root sign ambiguity)
    assert sp.expand(mys_derived - mys_table) == 0 or \
           sp.expand(mys_derived + mys_table) == 0, \
           (p, "Mys mismatch", mys_derived, mys_table)

